/*Programa: Imprimir los primeros 10 numeros naturales usando un ciclo 'for'
Objetivo: Mostrar los numeros del 1 al 10 en consola*/

#include <iostream> //Biblioteca estandar para entrada y salida
using namespace std;

int main(){
    //Usamos un ciclo 'for' para imprimir los primeros 10 numeros naturales
    for(int i = 1; i < 11; i++){
        cout<<i<<" ";
    }
    
    return 0;
}
